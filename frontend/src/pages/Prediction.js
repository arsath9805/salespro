import { useEffect, useState } from "react";
import Sidebar from "../components/Sidebar";
import createPlotlyComponent from "react-plotly.js/factory";
import Plotly from "plotly.js-dist-min";

import {
  collection,
  getDocs,
} from "firebase/firestore";

import { db } from "../firebase";

const Plot = createPlotlyComponent(Plotly);

function Prediction() {

  /* ===========================
        MAIN STATES
  =========================== */

  const [products, setProducts] = useState([]);
  const [selectedProduct, setSelectedProduct] =
    useState("");

  const [pastDates, setPastDates] =
    useState([]);

  const [pastSales, setPastSales] =
    useState([]);

  const [futureDates, setFutureDates] =
    useState([]);

  const [futureSales, setFutureSales] =
    useState([]);

  const [loading, setLoading] =
    useState(false);

  /* ===========================
       SUMMARY CARDS
  =========================== */

  const [currentWeekSales,
    setCurrentWeekSales] =
    useState(0);

  const [predictedWeekSales,
    setPredictedWeekSales] =
    useState(0);

  const [predictedRevenue,
    setPredictedRevenue] =
    useState(0);

  const [growthPercentage,
    setGrowthPercentage] =
    useState(0);

  /* ===========================
       DEMAND ANALYSIS
  =========================== */

  const [increaseProducts,
    setIncreaseProducts] =
    useState([]);

  const [decreaseProducts,
    setDecreaseProducts] =
    useState([]);

  const [restockProducts,
    setRestockProducts] =
    useState([]);

  /* ===========================
       AI INSIGHTS
  =========================== */

  const [insights,
    setInsights] =
    useState([]);

  /* ===========================
       LOAD PRODUCTS
  =========================== */

  useEffect(() => {

    const fetchProducts =
      async () => {

      const snapshot =
        await getDocs(
          collection(
            db,
            "products"
          )
        );

      const productList =
        snapshot.docs.map(
          (doc) => ({
            id: doc.id,
            ...doc.data(),
          })
        );

      setProducts(productList);

      if (
        productList.length > 0
      ) {

        setSelectedProduct(
          productList[0]
            .product_name
        );

      }

      /* RESTOCK LIST */

      const lowStock =
        productList.filter(
          (product) =>
            Number(
              product.quantity
            ) <=
            Number(
              product.threshold || 5
            )
        );

      setRestockProducts(
        lowStock
      );

    };

    fetchProducts();

  }, []);

  /* ===========================
      DOWNLOAD REPORT
  =========================== */

  const downloadReport =
    () => {

    const report = `
SALES FORECAST REPORT

Product:
${selectedProduct}

Current Week Sales:
${currentWeekSales}

Predicted Week Sales:
${predictedWeekSales}

Growth:
${growthPercentage.toFixed(2)}%

Predicted Revenue:
₹${predictedRevenue}

Generated by SalesPro AI
`;

    const blob =
      new Blob(
        [report],
        {
          type:
            "text/plain",
        }
      );

    const link =
      document.createElement(
        "a"
      );

    link.href =
      URL.createObjectURL(
        blob
      );

    link.download =
      "prediction-report.txt";

    link.click();

  };
  /* ===========================
      PREDICTION LOGIC
=========================== */

const handlePrediction =
  async () => {

  try {

    setLoading(true);

    const orderSnapshot =
      await getDocs(
        collection(
          db,
          "orders"
        )
      );

    const orders =
      orderSnapshot.docs
        .map((doc) =>
          doc.data()
        )
        .filter(
          (order) =>
            order.productName ===
            selectedProduct
        );

    if (
      orders.length === 0
    ) {

      alert(
        "No orders found for this product."
      );

      setLoading(false);

      return;

    }

    /* =====================
       GROUP SALES BY DAY
    ===================== */

    const dailySales = {};

    orders.forEach(
      (order) => {

        if (
          !order.orderedAt
        ) return;

        const date =
          order.orderedAt
            .toDate()
            .toISOString()
            .split("T")[0];

        dailySales[date] =
          (dailySales[
            date
          ] || 0) +
          Number(
            order.quantity || 0
          );

      }
    );

    let sortedDates =
      Object.keys(
        dailySales
      ).sort();

    sortedDates =
      sortedDates.slice(-7);

    const actualSales =
      sortedDates.map(
        (date) =>
          dailySales[
            date
          ]
      );

    setPastDates(
      sortedDates
    );

    setPastSales(
      actualSales
    );

    /* =====================
       CURRENT SALES
    ===================== */

    const currentTotal =
      actualSales.reduce(
        (a, b) =>
          a + b,
        0
      );

    setCurrentWeekSales(
      currentTotal
    );

    /* =====================
       AVERAGE GROWTH
    ===================== */

    let averageGrowth =
      0;

    if (
      actualSales.length > 1
    ) {

      let totalGrowth =
        0;

      for (
        let i = 1;
        i <
        actualSales.length;
        i++
      ) {

        totalGrowth +=
          actualSales[
            i
          ] -
          actualSales[
            i - 1
          ];

      }

      averageGrowth =
        totalGrowth /
        (
          actualSales.length -
          1
        );

    }

    /* =====================
       NEXT 7 DAYS
    ===================== */

    let current =
      actualSales[
        actualSales.length -
          1
      ] || 0;

    const nextDates =
      [];

    const nextSales =
      [];

    let lastDate =
      sortedDates.length >
      0
        ? new Date(
            sortedDates[
              sortedDates
                .length -
                1
            ]
          )
        : new Date();

    for (
      let i = 1;
      i <= 7;
      i++
    ) {

      current +=
        averageGrowth;

      const futureDate =
        new Date(
          lastDate
        );

      futureDate.setDate(
        futureDate.getDate() +
          i
      );

      nextDates.push(
        futureDate
          .toISOString()
          .split("T")[0]
      );

      nextSales.push(
        Math.max(
          0,
          Math.round(
            current
          )
        )
      );

    }

    setFutureDates(
      nextDates
    );

    setFutureSales(
      nextSales
    );

    /* =====================
       PREDICTED SALES
    ===================== */

    const predictedTotal =
      nextSales.reduce(
        (a, b) =>
          a + b,
        0
      );

    setPredictedWeekSales(
      predictedTotal
    );

    /* =====================
       PRODUCT INFO
    ===================== */

    const product =
      products.find(
        (p) =>
          p.product_name ===
          selectedProduct
      );

    const price =
      Number(
        product?.per_cost ||
        0
      );

    const revenue =
      predictedTotal *
      price;

    setPredictedRevenue(
      revenue
    );

    /* =====================
       GROWTH %
    ===================== */

    const growth =
      currentTotal
        ? (
            (
              predictedTotal -
              currentTotal
            ) /
            currentTotal
          ) *
          100
        : 0;

    setGrowthPercentage(
      growth
    );

    /* =====================
       INCREASE /
       DECREASE
    ===================== */

    if (
      growth > 0
    ) {

      setIncreaseProducts([
        selectedProduct,
      ]);

      setDecreaseProducts(
        []
      );

    } else {

      setDecreaseProducts([
        selectedProduct,
      ]);

      setIncreaseProducts(
        []
      );

    }

    /* =====================
       AI INSIGHTS
    ===================== */

    const aiInsights =
      [];

    if (
      growth > 10
    ) {

      aiInsights.push(
        `${selectedProduct} demand is expected to increase significantly.`
      );

    } else {

      aiInsights.push(
        `${selectedProduct} demand is stable.`
      );

    }

    if (
      product &&
      predictedTotal >
        product.quantity
    ) {

      aiInsights.push(
        `Immediate restocking recommended for ${selectedProduct}.`
      );

    }

    aiInsights.push(
      `Predicted revenue next week is ₹${revenue.toLocaleString()}.`
    );

    setInsights(
      aiInsights
    );

  }

  catch (error) {

    console.error(
      error
    );

    alert(
      "Prediction failed."
    );

  }

  finally {

    setLoading(false);

  }

};
return (
  <>
    <Sidebar />

    <div className="dashboard-content">

      <h2>📈 Sales Forecasting</h2>

      <p>
        AI-powered prediction for the next 7 days.
      </p>

      {/* =====================
          PRODUCT SELECT
      ===================== */}

      <div
        style={{
          background: "#fff",
          padding: "30px",
          borderRadius: "20px",
          marginTop: "30px",
          boxShadow:
            "0 10px 25px rgba(0,0,0,0.08)",
        }}
      >

        <label
          style={{
            fontWeight: "bold",
          }}
        >
          Select Product
        </label>

        <select
          value={selectedProduct}
          onChange={(e) =>
            setSelectedProduct(
              e.target.value
            )
          }
          style={{
            width: "100%",
            padding: "14px",
            marginTop: "10px",
            borderRadius: "10px",
            border:
              "1px solid #ddd",
          }}
        >
          {products.map(
            (product) => (
              <option
                key={product.id}
                value={
                  product.product_name
                }
              >
                {
                  product.product_name
                }
              </option>
            )
          )}
        </select>

        <button
          onClick={
            handlePrediction
          }
          disabled={loading}
          style={{
            marginTop: "25px",
            width: "100%",
            padding: "15px",
            border: "none",
            borderRadius: "12px",
            background: "#2563eb",
            color: "#fff",
            fontSize: "18px",
            fontWeight: "bold",
            cursor: "pointer",
          }}
        >
          {loading
            ? "Predicting..."
            : "🚀 Predict Next Week"}
        </button>

      </div>

      {/* =====================
          SUMMARY CARDS
      ===================== */}

      {pastDates.length > 0 && (

        <>

          <div
            style={{
              display: "grid",
              gridTemplateColumns:
                "repeat(auto-fit,minmax(220px,1fr))",
              gap: "20px",
              marginTop: "30px",
            }}
          >

            <div className="card">
              <h3>
                📦 Current Week
              </h3>

              <h1>
                {
                  currentWeekSales
                }
              </h1>
            </div>

            <div className="card">
              <h3>
                🔮 Predicted Week
              </h3>

              <h1>
                {
                  predictedWeekSales
                }
              </h1>
            </div>

            <div className="card">
              <h3>
                📈 Growth
              </h3>

              <h1>
                {
                  growthPercentage.toFixed(
                    2
                  )
                }%
              </h1>
            </div>

            <div className="card">
              <h3>
                💰 Revenue
              </h3>

              <h1>
                ₹
                {predictedRevenue.toLocaleString()}
              </h1>
            </div>

          </div>

          {/* =====================
              FORECAST CHART
          ===================== */}

          <div
            style={{
              background: "#fff",
              marginTop: "30px",
              padding: "20px",
              borderRadius: "20px",
            }}
          >

            <Plot
              data={[
                {
                  x: pastDates,
                  y: pastSales,
                  type: "scatter",
                  mode:
                    "lines+markers",
                  name:
                    "Actual Sales",
                  line: {
                    color:
                      "#2563eb",
                  },
                },

                {
                  x: futureDates,
                  y: futureSales,
                  type: "scatter",
                  mode:
                    "lines+markers",
                  name:
                    "Predicted Sales",
                  line: {
                    color:
                      "#ef4444",
                    dash:
                      "dash",
                  },
                },
              ]}

              layout={{
                title:
                  "Previous vs Predicted Sales",
                autosize: true,
                height: 500,

                xaxis: {
                  title:
                    "Date",
                },

                yaxis: {
                  title:
                    "Units Sold",
                },
              }}

              style={{
                width: "100%",
              }}
            />

          </div>

          {/* =====================
              INCREASED DEMAND
          ===================== */}

          <div
            style={{
              background: "#fff",
              marginTop: "30px",
              padding: "20px",
              borderRadius: "20px",
            }}
          >

            <h3>
              📈 Increased Demand
            </h3>

            {increaseProducts.map(
              (item) => (
                <p key={item}>
                  {item}
                </p>
              )
            )}

          </div>

          {/* =====================
              DECREASED DEMAND
          ===================== */}

          <div
            style={{
              background: "#fff",
              marginTop: "30px",
              padding: "20px",
              borderRadius: "20px",
            }}
          >

            <h3>
              📉 Decreased Demand
            </h3>

            {decreaseProducts.length ===
            0 ? (
              <p>
                No products
                expected to
                decline.
              </p>
            ) : (
              decreaseProducts.map(
                (item) => (
                  <p key={item}>
                    {item}
                  </p>
                )
              )
            )}

          </div>

          {/* =====================
              RESTOCK
          ===================== */}

          <div
            style={{
              background: "#fff",
              marginTop: "30px",
              padding: "20px",
              borderRadius: "20px",
            }}
          >

            <h3>
              🔄 Restock
              Recommendations
            </h3>

            {restockProducts.map(
              (product) => (

                <p
                  key={
                    product.id
                  }
                >

                  {
                    product.product_name
                  }

                  {" - "}

                  {product.quantity}

                  {" left"}

                </p>

              )
            )}

          </div>

          {/* =====================
              AI INSIGHTS
          ===================== */}

          <div
            style={{
              marginTop: "30px",
              background:
                "linear-gradient(135deg,#2563eb,#4f46e5)",

              color: "#fff",

              padding: "25px",

              borderRadius:
                "20px",
            }}
          >

            <h2>
              🤖 AI Insights
            </h2>

            <ul>

              {insights.map(
                (
                  item,
                  index
                ) => (

                  <li
                    key={index}
                    style={{
                      marginTop:
                        "12px",
                    }}
                  >
                    {item}
                  </li>

                )
              )}

            </ul>

          </div>

          {/* =====================
              DOWNLOAD REPORT
          ===================== */}

          <button
            onClick={
              downloadReport
            }
            style={{
              marginTop: "30px",
              width: "100%",
              padding: "15px",
              border: "none",
              borderRadius: "12px",
              background:
                "#16a34a",
              color: "#fff",
              fontSize: "18px",
              fontWeight: "bold",
              cursor: "pointer",
            }}
          >
            📄 Download
            Prediction Report
          </button>

        </>

      )}

    </div>
  </>
);

}

export default Prediction;